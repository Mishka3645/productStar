final class MyFriend
{
    private String informationAboutMyFriend = null;
    private String nameFriend = null;
    private int ageFriend = 0;

    String getInformationAboutMyFriend()
    {
        return this.informationAboutMyFriend;
    }
    void setInformationAboutMyFriend(String information)
    {
        this.informationAboutMyFriend = information;
    }

    String getNameFriend()
    {
        return this.nameFriend;
    }
    void setNameFriend(String name)
    {
        this.nameFriend = name;
    }

    int getAgeFriend()
    {
        return this.ageFriend;
    }
    void setAgeFriend(int age)
    {
        this.ageFriend = age;
    }
}
